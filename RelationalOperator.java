public class RelationalOperator {
    public static void main(String[] args) {
        int a = 10;
        int b = 10;
        int c = 20;
        System.out.println(a == b);
        System.out.println(a < b);
        System.out.println(a > b);
        System.out.println(a < c);
        System.out.println(a <= b);
        System.out.println(a >= b);
        System.out.println(a <= c);
        System.out.println(a >= b);
        System.out.println(a >= c);
        System.out.println(a != b);
        System.out.println(a != c);
    }
}
